package com.ncit.college.domain;

public class DashboardData {
	
	private Long adminCount;
	private Long collegeCount;
	private Long universityCount;
	private Long courseCount;
	private Long uniReviewCount;
	private Long colReviewCount;
	private Long courseReviewCount;
	public Long getAdminCount() {
		return adminCount;
	}
	public void setAdminCount(Long adminCount) {
		this.adminCount = adminCount;
	}
	public Long getCollegeCount() {
		return collegeCount;
	}
	public void setCollegeCount(Long collegeCount) {
		this.collegeCount = collegeCount;
	}
	public Long getUniversityCount() {
		return universityCount;
	}
	public void setUniversityCount(Long universityCount) {
		this.universityCount = universityCount;
	}
	public Long getCourseCount() {
		return courseCount;
	}
	public void setCourseCount(Long courseCount) {
		this.courseCount = courseCount;
	}
	public Long getUniReviewCount() {
		return uniReviewCount;
	}
	public void setUniReviewCount(Long uniReviewCount) {
		this.uniReviewCount = uniReviewCount;
	}
	public Long getColReviewCount() {
		return colReviewCount;
	}
	public void setColReviewCount(Long colReviewCount) {
		this.colReviewCount = colReviewCount;
	}
	public Long getCourseReviewCount() {
		return courseReviewCount;
	}
	public void setCourseReviewCount(Long courseReviewCount) {
		this.courseReviewCount = courseReviewCount;
	}
	
	

}
